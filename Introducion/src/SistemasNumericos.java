public class SistemasNumericos {
    public static void main(String[] args) {
        int numero;

        //decimal
        numero = 14;
        System.out.println("binario = " + numero);

        //binario hay que poner 0b y luego el numero en binario

        numero = 0b0011110;
        System.out.println("binario = " + numero);

        //octal hay que poner 0 y luego el numero en octal

        numero = 017;
        System.out.println("octal = " + numero);

        //exadecimal hay porne 0x y luego el numero en exadeciaml

        numero = 0X0E;

        System.out.println("exadecimal = " + numero);

    }
}
