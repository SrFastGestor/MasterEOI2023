public class NumeroReales {
    public static void main(String[] args) {
        // Tipo float
        float numeroFloat = 6.123123f;
        System.out.println("Foat= " + numeroFloat);
        System.out.println(Float.BYTES);
        System.out.println("Infinito positivo " + Float.POSITIVE_INFINITY);
        System.out.println("Infinito negativo " + Float.NEGATIVE_INFINITY);


        // Tipo dooble
        double numeroDouble = 6.465465465465456;
        System.out.println("Double= " + numeroDouble);
        System.out.println("lo que sea" + Double.NEGATIVE_INFINITY);
    
    
    
    }
}
